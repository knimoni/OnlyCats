        setget.setNama(Nama);
        String Jenis = model.getValueAt(index, 1).toString();
        setget.setJenis(Jenis);
        String Berat = model.getValueAt(index, 2).toString();
        setget.setBerat(Berat);
        String LamaPenitipan = model.getValueAt(index, 3).toString();
        setget.setLamaPenitipan(LamaPenitipan);
        String Tanggal1 = model.getValueAt(index, 4).toString();
        setget.setTanggal1(Tanggal1);
        String Tanggal2 = model.getValueAt(index, 5).toString();
        setget.setTanggal2(Tanggal2);
        String Jam = model.getValueAt(index, 6).toString();
        setget.setJam(Jam);
        
       
        detail.setVisible(true);
        detail.pack();
