        detail.Jam.setText(setget.getJam());
        detail.Tanggal1.setText(setget.getTanggal1());
        detail.Tanggal2.setText(setget.getTanggal2());
        detail.Jenis.setText(setget.getJenis());
        detail.Lama.setText(setget.getLamaPenitipan());
