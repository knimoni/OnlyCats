    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        namapemilik.setText(namapemilik.getText());
        setget.setNama(namapemilik.getText());
        String Berat = berat.getSelectedItem().toString();
        setget.setBerat(Berat);
        String Tanggal1 = ((JTextField)tanggal1.getDateEditor().getUiComponent()).getText();
        setget.setTanggal1(Tanggal1);
        String Tanggal2 = ((JTextField)tanggal2.getDateEditor().getUiComponent()).getText();
        setget.setTanggal2(Tanggal2);
        String LamaPenitipan = lamapenitipan.getSelection().getActionCommand();
        setget.setLamaPenitipan(LamaPenitipan);
        String Jenis = jeniskucing.getSelection().getActionCommand();
        setget.setJenis(Jenis);
        String Jam = jam.getSelectedItem().toString();
        setget.setJam(Jam);
        if (namapemilik.getText().trim().isEmpty() || "Pilih...".equals(Berat) || "Pilih...".equals(Jam) || Tanggal2.trim().isEmpty() && Tanggal1.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "ISI DENGAN LENGKAP", "WARNING", HEIGHT);
        }
        else {
            JOptionPane.showMessageDialog(null, "data berhasil ditambahkan", "SUCCESSFULLY", HEIGHT);
            DefaultTableModel model = (DefaultTableModel) daftar.getModel();
            model.addRow(new Object[]{setget.getNama(), setget.getJenis(), setget.getBerat(), setget.getLamaPenitipan(), setget.getTanggal1(), setget.getTanggal2(), setget.getJam()});
        }
    }//GEN-LAST:event_addActionPerformed

